package com.exercise.entity;

public class Weather {

	private String date;
	private String city;
	private String description;
	private String tempF;
	private String tempC;
	private String timeSunrise;
	private String timeSunset;
	
	
	public String getDate() {
		return date;
	}
	
	public void setDate(final String date) {
		this.date = date;
	}
	
	public String getCity() {
		return city;
	}
	
	public void setCity(final String city) {
		this.city = city;
	}
	
	public String getDescription() {
		return description;
	}
	
	public void setDescription(final String description) {
		this.description = description;
	}
	
	public String getTempF() {
		return tempF;
	}
	
	public void setTempF(final String tempF) {
		this.tempF = tempF;
	}
	
	public String getTempC() {
		return tempC;
	}
	
	public void setTempC(final String tempC) {
		this.tempC = tempC;
	}
	
	public String getTimeSunrise() {
		return timeSunrise;
	}
	
	public void setTimeSunrise(final String timeSunrise) {
		this.timeSunrise = timeSunrise;
	}
	
	public String getTimeSunset() {
		return timeSunset;
	}
	
	public void setTimeSunset(final String timeSunset) {
		this.timeSunset = timeSunset;
	}
	
	
	
}
